package com.ypz.killetom.basedevsdk.constant

object MemoryRadioConstant {

    val MEMORY_RADIO  by lazy { 1024 }

    val BYTE by lazy { 1 }

    val KB by lazy { BYTE * MEMORY_RADIO }

    val MB by lazy { KB * MEMORY_RADIO }

    val GB by lazy { MB * MEMORY_RADIO }

    val TB by lazy { GB * MEMORY_RADIO }


}